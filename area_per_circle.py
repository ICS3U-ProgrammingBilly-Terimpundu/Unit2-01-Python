# This program calculates and displays the area and perimeter of a 
# circle with radius 60 cm.
import math
 
 
def main():
  print("For a circle that has a radius")
  print("of 60 cm:")
  print("")
  print("Area = {} cm^2". format (math.pi*60**2))
  print("Perimeter = {} cm". format (2*math.pi*60))

if __name__ == "__main__":
  main()
